# print(<значення>) – виводить значення на екран
print("Hello, world!")
# str(<об'єкт>) – виводить стислу структуру об'єкта
str(mtcars)
# c(<елементи>) – об'єднує елементи в вектор
c(1, 2, 3, 4, 5)
# seq(<від>, <до>, <крок>) – створює послідовність чисел
seq(1, 10, by = 2)
# rep(<значення>, <кількість>) – повторює значення певну кількість разів
rep(1, 5)
# numeric(<довжина>) – створює числовий вектор складений з дійсних чисел
numeric(5)
# names(<вектор>) – присвоює або повертає імена елементів вектора
x <- c(a = 1, b = 2, c = 3)
names(x)
# rbind(<матриця1>, <матриця2>) – об'єднує рядки матриць
rbind(c(1, 2), c(3, 4))
# cbind(<матриця1>, <матриця2>) – об'єднує стовпці матриць
cbind(c(1, 2), c(3, 4))
# matrix(<дані>, nrow=<кількість рядків>, ncol=<кількість стовпців>) – створює матрицю
matrix(1:6, nrow=2, ncol=3)
# dimnames(<матриця>) – повертає або присвоює імена рядків і стовпців матриці
m <- matrix(1:4, nrow=2)
dimnames(m) <- list(c("row1", "row2"), c("col1", "col2"))
# rownames(<матриця>) – повертає або присвоює імена рядків матриці
rownames(m)
# colnames(<матриця>) – повертає або присвоює імена стовпців матриці
colnames(m)
# diag(<матриця або вектор>) – створює або повертає діагональну матрицю
diag(1:3)
# solve(<матриця>) – обчислює обернену матрицю
m <- matrix(c(1, 2, 3, 4), nrow=2)
solve(m)
# min(<вектор>) – знаходить мінімальне значення у векторі
min(c(1, 2, 3, 4, 5))
# max(<вектор>) – знаходить максимальне значення у векторі
max(c(1, 2, 3, 4, 5))
# sum(<вектор>) – обчислює суму елементів вектора
sum(c(1, 2, 3, 4, 5))
# length(<вектор>) – повертає довжину (кількість елементів) вектора
length(c(1, 2, 3, 4, 5))
# t(<матриця>) – обчислює транспоновану матрицю
t(matrix(1:6, nrow=2))
# order(<вектор>) – повертає індекси, що сортують вектор
order(c(3, 1, 2))
# sort(<вектор>) – сортує вектор за зростанням або спаданням
sort(c(3, 1, 2))

